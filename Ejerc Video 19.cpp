// Sentencia de repeticion  while

#include<iostream>
using namespace std;

int main(){
	int i ;
	
	i = 12;
	
	while (i>=1){
		cout << i << endl;
		i-- ;
	}
	
	system("pause"); // util para los .exe ya que impide que se cierre la ventan automaticamente, al no pedir un valor por pantalla
	return 0;
}
